package Top.HARD.Coding_Questions;

// Definition for singly-linked list.
public class ListNode {
	
		int data;
	    ListNode next;
	    
	    ListNode() {}
	    
	    ListNode(int data) { 
	   	 	this.data = data; 
	   	 }
	    
	    ListNode(int data, ListNode next) { 
	   	 	this.data = data; 
	   	 	this.next = next; 
	   	 }
}
